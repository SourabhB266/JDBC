package user_assignment;

public class User {
	
	private int id;
	private String username;
	private String email;
	private String password;
	private String facebook_pass;
	private String insta_pass;
	private String twitter_pass;
	private String whatsapp_pass;
	private String snapchat_pass;
	private String address; 
	
	public int getId() {
		return id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getFacebook_pass() {
		return facebook_pass;
	}
	public void setFacebook_pass(String facebook_pass) {
		this.facebook_pass = facebook_pass;
	}
	public String getInsta_pass() {
		return insta_pass;
	}
	public void setInsta_pass(String insta_pass) {
		this.insta_pass = insta_pass;
	}
	public String getTwitter_pass() {
		return twitter_pass;
	}
	public void setTwitter_pass(String twitter_pass) {
		this.twitter_pass = twitter_pass;
	}
	public String getWhatsapp_pass() {
		return whatsapp_pass;
	}
	public void setWhatsapp_pass(String whatsapp_pass) {
		this.whatsapp_pass = whatsapp_pass;
	}
	public String getSnapchat_pass() {
		return snapchat_pass;
	}
	public void setSnapchat_pass(String snapchat_pass) {
		this.snapchat_pass = snapchat_pass;
	}
	
	
}
